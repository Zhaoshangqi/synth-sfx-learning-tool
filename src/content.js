function synthMappingsForLesson(track, titleZh) {
  const mappings = {};
  if (track.synthFocus.includes('Serum')) {
    mappings.serum = [`在 Serum 中从 ${titleZh} 的核心参数开始，先关闭复杂 FX。`, '把一个关键参数映射到 macro 或 LFO，渲染低/中/高三版。'];
  }
  if (track.synthFocus.includes('Phase Plant')) {
    mappings.phasePlant = [`在 Phase Plant 中用 generator/modulator 分清 ${titleZh} 的声源和调制。`, '优先用模块分组表达 transient、body、texture 或 tail。'];
  }
  if (track.synthFocus.includes('Vital')) {
    mappings.vital = [`在 Vital 中利用可视化调制观察 ${titleZh} 对频谱或时间形状的影响。`, '用 spectral/wavetable/filter 视图确认听到的变化。'];
  }
  if (!mappings.serum && !mappings.phasePlant && !mappings.vital) {
    mappings.serum = [`用 Serum 的 Basic Shapes 做 ${titleZh} 听辨样本。`, '关闭复杂 FX，只保留声源、包络和必要滤波。'];
    mappings.phasePlant = [`用 Phase Plant 建一个最小 generator patch，专注观察 ${titleZh}。`, '渲染 A/B 后在 REAPER 中比较。'];
    mappings.vital = [`用 Vital 的基础波形和可视化反馈复查 ${titleZh}。`, '只改一个参数，听变化是否和图像一致。'];
  }
  return mappings;
}

