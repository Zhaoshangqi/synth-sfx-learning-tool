function sourceReliabilityForTip(sourceIds = []) {
  const hasResearch = sourceIds.some((sourceId) => sourceId.endsWith('-paper'));
  const hasOfficial = sourceIds.some((sourceId) => ['xfer-serum-2-official', 'kilohearts-phase-plant-docs', 'kilohearts-modulation-docs', 'vital-official', 'reaper-official-videos'].includes(sourceId));
  const hasYoutubeSeed = sourceIds.some((sourceId) => sourceId.startsWith('youtube-'));

  if (hasResearch && hasOfficial) return '高：官方功能依据加研究资料支撑原理，最终仍以 REAPER A/B 和响度匹配验证听感。';
  if (hasOfficial) return '中高：官方资料能验证插件能力和工作流，具体音效好坏需要按卡片步骤做 A/B。';
  if (hasYoutubeSeed) return '中：YouTube 可提供做法灵感，但必须用参数边界、来源对照和自己的渲染结果验证。';
  return '中：属于可复现实战经验，不能当成绝对规则，需要按听感检查逐项确认。';
}

